# Basic examples

x = lambda a: a * 10
print(x(5),"=================================1")

###########################################################

x = lambda a,b: a * b
print(x(5, 25),"=================================2")

###########################################################

def testfunction1(n):
    x = lambda a: a * n
    return x

result = testfunction1(3)

print(result(4),"=================================3")

###########################################################

n = lambda a: "Even" if a % 2 == 0 else "Odd"
print(n(6),"=================================4")

###########################################################

n = [1, 2, 3, 4, 5, 6]
even = filter(lambda x: x % 2 == 0, n)
print(list(even),"=================================5")

###########################################################

a = [1, 2, 3, 4]
b = map(lambda x: x * 2, a)
print(list(b),"=================================6")