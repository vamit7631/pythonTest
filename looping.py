# List example
my_list = [10, 20, 30, 40]

for item in my_list:
    print(item)

###########################################################

# Dictionary example

mydict =  {'name': 'Alice', 'age': 25, 'city': 'New York'}

for key,value in mydict.items():
    print(key, "=====", value)

###########################################################

# Tuple example

my_tuple = ('apple', 'banana', 'cherry')

for item in my_tuple:
    print(item,"====tuple_item")

###########################################################

# Set example

my_set = {1, 2, 3, 4}

for y in my_set:
    print(y,"====set_item") 